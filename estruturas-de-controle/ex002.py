# Crie uma lista com os nomes de cinco amigos e, usando um for, exiba uma mensagem de saudação para cada um.

# Inicializa uma lista de 5 nomes de amigos
friends = ['Luis', 'Paulo', 'Caio', 'Pires', 'João Victor']

# Realiza uma iteração na lista, exibindo no terminal uma saudação para cada amigo.
