# Crie um programa que solicite ao usuário que insira seu nome e idade. Em seguida, 
# exiba uma mensagem dizendo: "Olá, [nome], você tem [idade] anos!".

idade = int(input("Insira a sua idade atual: ")) # Inicializa e recebe pelo terminal a idade do usuário

nome = input("Insira o seu nome: ") # Inicializa e recebe pelo terminal o nome do usário

print(f"Olá, {nome}, você tem {idade} anos!") # Exibe as variáveis no terminal